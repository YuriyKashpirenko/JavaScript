// constructor for the book
function Book(title, 
              description,
              smallThumbnail,
              amount, 
              authors,
              previewLink
              ){
  this.title = title;
  this.description = description;
  this.smallThumbnail = smallThumbnail;
  this.amount = amount;
  this.authors = authors;
  this.previewLink = previewLink;
}
